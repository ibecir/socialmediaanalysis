package ba.ibu.edu.model;

public class DayAggregation {

	public long occurences;

	public DayAggregation(long occurences) {
		super();
		this.occurences = occurences;
	}

	public long getOccurences() {
		return occurences;
	}

	public void setOccurences(long occurences) {
		this.occurences = occurences;
	}

}
